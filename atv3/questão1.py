num = int(input("Digite um número de 0 a 100: "))

if num <= 25:
    print(f"{num} está na faixa de 0 a 25.")


if num >= 26 and num <= 50:
    print(f"{num} está na faixa de 26 a 50.")


if num >= 51 and num <= 75:
    print(f"{num} está na faixa de 51 a 75.")


if num >= 76 and num <= 100:
    print(f"{num} está na faixa de 76 a 100.")
